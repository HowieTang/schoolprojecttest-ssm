package com.yh.model.student;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 * 学生请假实体类
 * 
 */
public class StudentLeaveModel implements Serializable{
	private static final long serialVersionUID = 1L;
    //学生请假表ID
	private String sleaveId;
	//请假人姓名
	private String name;
	//请假原因
	private String leaveReason;
    //请假天数
	private String leaveDay;
	//请假类型
	private String type;
	//老师id
	private String tname;
	//请假开始时间
	private String startTime;
	//请假结束时间
	private String endTime;
	//状态
	private int state;
   //是否删除
	private int flagDel;
	//关联教师表获取教师ID
	private List<String> teacherId;
	
	public String getSleaveId(){
		return sleaveId;
	}

	public void setSleaveId(String sleaveId) {
		this.sleaveId = sleaveId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLeaveReason() {
		return leaveReason;
	}

	public void setLeaveReason(String leaveReason) {
		this.leaveReason = leaveReason;
	}

	public String getLeaveDay() {
		return leaveDay;
	}

	public void setLeaveDay(String leaveDay) {
		this.leaveDay = leaveDay;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public int getFlagDel() {
		return flagDel;
	}

	public void setFlagDel(int flagDel) {
		this.flagDel = flagDel;
	}

	public List<String> getTeacherId() {
		return teacherId;
	}

	public void setTeacherId(List<String> teacherId) {
		this.teacherId = teacherId;
	}

	public String getTname() {
		return tname;
	}

	public void setTname(String tname) {
		this.tname = tname;
	}

}
