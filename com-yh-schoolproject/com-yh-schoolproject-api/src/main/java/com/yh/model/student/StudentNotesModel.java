package com.yh.model.student;

import java.io.Serializable;
import java.util.Date;

public class StudentNotesModel implements Serializable{

	private static final long serialVersionUID = -836732952658190514L;

	//学生笔记id
	private String stuNotesId;
	
	//学生笔记标题
	private String stuNotesName;
	
	//学生笔记内容
	private String stuNotesText;
	
	//学生笔记投稿类型
	private String stuNewsType;

	//学生笔记修改时间
	private Date stuNotesUbt;
	
	//学生笔记提交时间
	private Date stuNotesSubt;

	public String getStuNotesId() {
		return stuNotesId;
	}

	public void setStuNotesId(String stuNotesId) {
		this.stuNotesId = stuNotesId;
	}

	public String getStuNotesName() {
		return stuNotesName;
	}

	public void setStuNotesName(String stuNotesName) {
		this.stuNotesName = stuNotesName;
	}

	public String getStuNotesText() {
		return stuNotesText;
	}

	public void setStuNotesText(String stuNotesText) {
		this.stuNotesText = stuNotesText;
	}

	public Date getStuNotesUbt() {
		return stuNotesUbt;
	}

	public void setStuNotesUbt(Date stuNotesUbt) {
		this.stuNotesUbt = stuNotesUbt;
	}

	public Date getStuNotesSubt() {
		return stuNotesSubt;
	}

	public void setStuNotesSubt(Date stuNotesSubt) {
		this.stuNotesSubt = stuNotesSubt;
	}

	public String getStuNewsType() {
		return stuNewsType;
	}

	public void setStuNewsType(String stuNewsType) {
		this.stuNewsType = stuNewsType;
	}

}
